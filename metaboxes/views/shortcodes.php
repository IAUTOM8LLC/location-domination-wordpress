<p>You can use these shortcodes on any page or title created with the Location Domination.</p>
<div>
    <ul>
        <li><code>[city]</code>Use to get current city</li>
        <li><code>[state]</code>Use to get current state</li>
        <li><code>[state_abbr]</code>Use to get current state abbreviation, ie. NY</li>
        <li><code>[county]</code>Use to get current county</li>
        <li><code>[zip_codes]</code>Use to get a list of all ZIP codes related to the city</li>
        <li><code>[breadcrumb]</code>Use to display a breadcrumb by city, state, county for current page.</li>
    </ul>
</div>